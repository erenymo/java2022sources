package conditionals;

public class conditionals {
    public static void main(String[] args) {
        int sayi = 34;
        if(sayi<20){
            System.out.println("Girdiğiniz sayı 20den küçüktür");
        }else if(sayi==20){
            System.out.println("Girdiğiniz sayı 20ye eşittir");
        }else{
            System.out.println("Girdiğiniz sayı 20den büyüktür");
        }
    }
}
